public class SWITCH1 {
    public static void main (String args[]){
        int i = 10;
        switch (i){
            case 10:
            System.out.println("Ten");
            break;
            case 20:
            System.out.println("Twenty");
            break;
            case 30:
            System.out.println("Thirty");
            break;
            default:
            System.out.println("Invalid input");
        }
    }    
}
